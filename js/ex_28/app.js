var age = 21;
if (age < 2) {
    document.write("He is a baby.")
}
else if (age >= 2 && age < 4) {
    document.write("He is a toddler")
}

else if (age >= 4 && age < 13) {
    document.write("He is a Kid")
}

else if (age >= 13 && age < 20) {
    document.write("He is a Teenager")
}
else if (age >= 20 && age < 65) {
    document.write("He is an Adult.") 
}
else if (age <= 65) {
    document.write("He is an elder")
} 